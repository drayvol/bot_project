"""Извлечение балла из текстового ответа LLM-верификации.

Модель просят завершить разбор строкой «Итоговый балл: X», но формат
гуляет — поэтому каскад паттернов от строгих к общим.
"""

import re

_PATTERNS = [
    # "Итоговый балл: 1" / "**Итоговый балл:** 1" / перенос строки между словами
    r'итоговый\s+балл[^01]{0,25}([01])\b',
    r'итоговый\s+балл[^01\n]{0,20}([01])\b',
    # "**1 балл**" / "0 баллов"
    r'\*?\*?([01])\*?\*?\s+балл',
    # "балл: 1" / "балл — 0"
    r'балл[^01\n]{0,10}([01])\b',
    r'score[^01\n]{0,10}([01])\b',
    r'оценка[^01\n]{0,10}([01])\b',
    # "1/1" / "0/1"
    r'\b([01])/1\b',
]


def extract_score_from_verification(text: str) -> int | None:
    """0 / 1, либо None — балл в тексте не найден."""
    if not text:
        return None
    for pat in _PATTERNS:
        m = re.search(pat, text, re.IGNORECASE | re.MULTILINE)
        if m:
            return int(m.group(1))
    return None
